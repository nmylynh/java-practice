class DoubleLongFloat {
    public static void main(String[] args){
        // integers can only be whole numbers, you cannot have a decimal
        // there's also a max size an int can be, if it's really long its called a long integer
        int a = 9;
        // long int
        long b = 99999999999999L;
        // decimals
        float c = 2.5F;
        // double is a really long float
        double d = 5.4;

        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
    }
}